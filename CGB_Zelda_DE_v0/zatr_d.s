VRAMST_ATR
		LD	A,(CGBFLG)
		AND	A		; カラーゲームボーイ？
		RET	Z		; ＮＯ
;------------------------------------------------------------------------
		LD	A,$01
		LD	(VBK),A		; バンク（１）セット

		LD	HL,VRDATA_ATR
		LD	B,$0
		LD	A,(VRFLG)
		SLA	A
		LD	C,A
		ADD	HL,BC
		LD	A,(HLI)
		LD	E,A		; データアドレスＨＩＧＨ
		LD	A,(HL)
		LD	D,A		; データアドレスＬＯＷ

		CALL	VRAMTR1		; Ｖ−ＲＡＭ トラスファー

		XOR	A
		LD	(VBK),A		; バンク（０）セット
		RET

